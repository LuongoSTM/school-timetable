function toggleDarkMode() {
    const body = document.body;
    const toggle = document.getElementById('dark-mode-toggle');
    
    if (body.hasAttribute('data-theme')) {
        body.removeAttribute('data-theme');
        toggle.classList.remove('active');
        localStorage.setItem('darkMode', 'false');
    } else {
        body.setAttribute('data-theme', 'dark');
        toggle.classList.add('active');
        localStorage.setItem('darkMode', 'true');
    }
}

// Event listeners
document.addEventListener('DOMContentLoaded', function() {
    // Load dark mode preference
    if (localStorage.getItem('darkMode') === 'true') {
        document.body.setAttribute('data-theme', 'dark');
        const toggle = document.getElementById('dark-mode-toggle');
        if (toggle) toggle.classList.add('active');
    }
    
    // Week button event listeners - Updated for control buttons
    document.querySelectorAll('.control-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const week = this.dataset.week;
            if (week === 'auto') {
                currentMode = 'auto';
            } else if (week === 'A' || week === 'B') {
                currentMode = 'manual';
                currentWeek = week;
            }
            updateWeekButtons();
            generateTimetable();
            updateTeacherBoxes();
        });
    });
    
    // Legacy week button event listeners (for backward compatibility)
    document.querySelectorAll('.week-btn').forEach(btn => {
        btn.addEventListener('click', function() {
            const week = this.dataset.week;
            if (week === 'auto') {
                currentMode = 'auto';
            } else {
                currentMode = 'manual';
                currentWeek = week;
            }
            updateWeekButtons();
            generateTimetable();
            updateTeacherBoxes();
        });
    });
    
    // Dark mode toggle
    const darkModeToggle = document.getElementById('dark-mode-toggle');
    if (darkModeToggle) {
        darkModeToggle.addEventListener('click', toggleDarkMode);
    }
    
    // Modal event listeners
    const modalClose = document.getElementById('modal-close');
    const modal = document.getElementById('subject-modal');
    const saveBtn = document.getElementById('save-request');
    const clearBtn = document.getElementById('clear-request');
    const notesField = document.getElementById('notes-field');
    const charCount = document.getElementById('char-count');
    
    if (modalClose) {
        modalClose.addEventListener('click', hideModal);
    }
    
    if (modal) {
        modal.addEventListener('click', function(e) {
            if (e.target === this) {
                hideModal();
            }
        });
    }
    
    if (saveBtn) {
        saveBtn.addEventListener('click', saveRequest);
    }
    
    if (clearBtn) {
        clearBtn.addEventListener('click', clearRequest);
    }
    
    if (notesField && charCount) {
        notesField.addEventListener('input', function() {
            charCount.textContent = this.value.length;
        });
    }
    
    // Communications field word count
    const communicationsField = document.getElementById('communications-field');
    const wordCount = document.getElementById('word-count');
    
    if (communicationsField && wordCount) {
        communicationsField.addEventListener('input', function() {
            const words = this.value.trim().split(/\s+/).filter(word => word.length > 0);
            wordCount.textContent = this.value.trim() === '' ? '0' : words.length;
        });
    }
    
    // Initial setup
    updateWeekButtons();
    generateTimetable();
    updateClock();
    updateTeacherBoxes();
    
    // Display current user if logged in
    const currentUser = sessionStorage.getItem('currentUser');
    const userInfoElement = document.getElementById('user-info');
    if (currentUser && userInfoElement) {
        userInfoElement.textContent = `Logged in as: ${currentUser}`;
    }
    
    // Update every second
    setInterval(() => {
        updateClock();
        updateTeacherBoxes();
    }, 1000);
});

